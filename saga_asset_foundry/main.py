@app.get("/api/assets/map/{category}/{key}")
def get_asset_mapping(category: str, key: str):
    key = key.upper()
    if category == "biome":
        return {"url": f"http://localhost:8012{BIOME_MAP.get(key, '/public/floor/grass_0_new.png')}"}
    if category == "object":
        return {"url": f"http://localhost:8012{OBJ_MAP.get(key, '/public/objects/barrel.png')}"}
    return {"error": "Category not found"}

class AssetRegister(BaseModel):
    asset_id: str
    source_path: str # Path to the image generated by the AI tool

@app.get("/health")
def health():
    return {"status": "ok", "module": "Asset Foundry", "port": 8012}

@app.post("/api/assets/register")
async def register_asset(reg: AssetRegister):
    """
    Copies an AI-generated image to the foundry storage and returns the public URL.
    """
    if not os.path.exists(reg.source_path):
        raise HTTPException(status_code=404, detail="Source image not found")
    
    file_ext = os.path.splitext(reg.source_path)[1]
    target_filename = f"{reg.asset_id}{file_ext}"
    target_path = os.path.join(ASSETS_DIR, target_filename)
    
    try:
        shutil.copy2(reg.source_path, target_path)
        return {"url": f"http://localhost:8012/assets/{target_filename}"}
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Failed to copy asset: {str(e)}")

if __name__ == "__main__":
    uvicorn.run(app, host="0.0.0.0", port=8012)
