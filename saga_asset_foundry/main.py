import os
import shutil
from fastapi import FastAPI, HTTPException
from fastapi.staticfiles import StaticFiles
from fastapi.middleware.cors import CORSMiddleware
from pydantic import BaseModel
import uvicorn

app = FastAPI(title="S.A.G.A. Asset Foundry", description="Dynamic AI Asset Server")

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

# Directory to store generated assets
ASSETS_DIR = os.path.join(os.path.dirname(os.path.abspath(__file__)), "storage")
os.makedirs(ASSETS_DIR, exist_ok=True)

# MOUNT PUBLIC DIRECTORY
PUBLIC_DIR = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "public")

# Mount the storage directory and public directory
app.mount("/assets", StaticFiles(directory=ASSETS_DIR), name="assets")
app.mount("/public", StaticFiles(directory=PUBLIC_DIR), name="public")

